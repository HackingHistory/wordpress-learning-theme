  <h3>Victory!</h3>
  <p>As recently as July 2016, the KCC won the battle for the designation of KNP as a mixed status heritage site, thereby considered “officially” now as both naturally and culturally significant. While UNESCO was initially convinced of Khangchendzonga’s natural significance, they were dismissive of the likelihood that the KNP held any cultural significance because there was, they believed, no tangible proof of it (e.g., archaeological evidence). And so, the KCC had a frustrating and tedious bureaucratic journey ahead. They argued the many points which are considered within the pages of this website, whereby the cultural significance extends beyond the tangible to the very intangible worldviews and structures by which the Sikamese comprehend their lives and their history. Finally, however, UNESCO has come to see that a mountain is much more than a mountain in this context. Hopefully now or sometime soon the resources at the disposal of UNESCO will come to aid the Sikamese in their bid for conservation and growth of their tourism sector. </p>


